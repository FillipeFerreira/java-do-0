package maratona_java_Dev_Dojo;

public class Execicio_02_operadores {

    public static void main(String[] args) {
        //operadores  + - / * //numeros inteiros.

        int numeros = 10;
        int numero3 = 20;


        System.out.println(numero3 * numeros);
        System.out.println(numero3 / numeros);
        System.out.println(numero3 + numeros);
        System.out.println(numero3 - numeros);


    }
}